bool map::checkMapCase(unsigned int dx, unsigned int dy)
{
	if (dx < 0 || dx >= m_vecMap[0].size() || dy < 0 || dy >= m_vecMap.size())
	{
		return false;
	}

	return m_vecMap[dy][dx] != 0;
}

size_t map::getHeight()
{
	return m_vecMap.size();
}

size_t map::getWidth()
{
	return m_vecMap[0].size();
}